package org.improving.tag;

public class Person {

    private String name, dateOfBirth, address, city, state;
    int zip;

    public String getName() {
        return name;
    }

    public String getDateOfBirth() {

        return dateOfBirth;
    }

    public String getAddress() {

        return address;
    }

    public String getCity() {

        return city;
    }

    public String getState() {

        return state;
    }

    public int getZip() {

        return zip;
    }

}
